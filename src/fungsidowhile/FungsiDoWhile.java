public class FungsiDoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //deklarasi variabel
        int bil = 1;
        //rumus perulangan
        do{
            System.out.print(bil+" ");
            bil++;
        //perintah batas perulangan
        }while (bil<=10);
        }
    }
    

